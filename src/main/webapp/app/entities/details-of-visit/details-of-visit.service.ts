import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IDetailsOfVisit } from '@/shared/model/details-of-visit.model';

const baseApiUrl = 'api/details-of-visits';

export default class DetailsOfVisitService {
  public find(id: number): Promise<IDetailsOfVisit> {
    return new Promise<IDetailsOfVisit>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDetailsOfVisit): Promise<IDetailsOfVisit> {
    return new Promise<IDetailsOfVisit>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDetailsOfVisit): Promise<IDetailsOfVisit> {
    return new Promise<IDetailsOfVisit>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IDetailsOfVisit): Promise<IDetailsOfVisit> {
    return new Promise<IDetailsOfVisit>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
