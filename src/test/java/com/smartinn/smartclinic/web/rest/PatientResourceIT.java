    @Test
    @Transactional
    void putNewPatient() throws Exception {
        // Initialize the database
        patientRepository.saveAndFlush(patient);

        int databaseSizeBeforeUpdate = patientRepository.findAll().size();

        // Update the patient
        Patient updatedPatient = patientRepository.findById(patient.getId()).get();
        // Disconnect from session so that the updates on updatedPatient are not directly saved in db
        em.detach(updatedPatient);
        updatedPatient
            .firstName(UPDATED_FIRST_NAME)
            .lastName(UPDATED_LAST_NAME)
            .pesel(UPDATED_PESEL)
            .firstFatherName(UPDATED_FIRST_FATHER_NAME)
            .contactNumber(UPDATED_CONTACT_NUMBER)
            .placeOfResidence(UPDATED_PLACE_OF_RESIDENCE)
            .dateOfBirth(UPDATED_DATE_OF_BIRTH)
            .bloodGroup(UPDATED_BLOOD_GROUP)
            .phoneNumber(UPDATED_PHONE_NUMBER);

        restPatientMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedPatient.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedPatient))
            )
            .andExpect(status().isOk());

        // Validate the Patient in the database
        List<Patient> patientList = patientRepository.findAll();
        assertThat(patientList).hasSize(databaseSizeBeforeUpdate);
        Patient testPatient = patientList.get(patientList.size() - 1);
        assertThat(testPatient.getFirstName()).isEqualTo(UPDATED_FIRST_NAME);
        assertThat(testPatient.getLastName()).isEqualTo(UPDATED_LAST_NAME);
        assertThat(testPatient.getPesel()).isEqualTo(UPDATED_PESEL);
        assertThat(testPatient.getFirstFatherName()).isEqualTo(UPDATED_FIRST_FATHER_NAME);
        assertThat(testPatient.getContactNumber()).isEqualTo(UPDATED_CONTACT_NUMBER);
        assertThat(testPatient.getPlaceOfResidence()).isEqualTo(UPDATED_PLACE_OF_RESIDENCE);
        assertThat(testPatient.getDateOfBirth()).isEqualTo(UPDATED_DATE_OF_BIRTH);
        assertThat(testPatient.getBloodGroup()).isEqualTo(UPDATED_BLOOD_GROUP);
        assertThat(testPatient.getPhoneNumber()).isEqualTo(UPDATED_PHONE_NUMBER);
    }

